    private void subtractButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subtractButtonActionPerformed
        String selectedDestination = destinationBox.getSelectedItem().toString();
        if (selectedDestination.length() > 1) {
            if (DestinationList.isCompleted(selectedDestination)) { //if completed
                if (DestinationList.playerCompleted(selectedDestination, player)) {//if player owns
                    int score = DestinationList.removeDestination(selectedDestination, player);
                    player.removeDestination(score, selectedDestination);
                    update();
                } else { //if not player owned
                    status.setText(DestinationList.getPlayerCompleted(selectedDestination) + " already added this!");
                }
            } else { //if not completed
                //if it is not completed, then we should add it to the uncompleted list
                //when adding, we should check the uncompleted list

                //check here, if on uncompletedlist then do not subtract.
                if (DestinationList.isUncompleted(selectedDestination)) { //if alreadysubtracted
                    status.setText(DestinationList.getPlayerUncompleted(selectedDestination) + " already subtracted this!");
                } else { //in not already subtracted and not completed... then we must subtract
                    //check for bonus first
                    if (DestinationList.isBonus(selectedDestination)) { //if is a bonus
                        int score = DestinationList.parsePoints(selectedDestination);
                        if (player.hasBonus(score)) { // if player already has it //remove it
                            player.removeBonus(score, selectedDestination);
                            update();
                        } else {
                            status.setText("Cannot subtract a bonus!");
                        }
                    } else {
                        int score = DestinationList.addUncompleted(selectedDestination, player);
                        player.uncompleteDestination(score, selectedDestination);
                        update();
                    }
                }
            }

        }
    }//GEN-LAST:event_subtractButtonActionPerformed

