    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String selectedDestination = destinationBox.getSelectedItem().toString();
        if (selectedDestination.length() > 1) {
            if (DestinationList.isCompleted(selectedDestination)) { //if on completed list
                status.setText(DestinationList.getPlayerCompleted(selectedDestination) + " already added this!");
            } else { //if not on list
                if (DestinationList.isUncompleted(selectedDestination)) { //if on UNcompleted list
                    //check if player owned
                    if (DestinationList.playerUncompleted(selectedDestination, player)) {
                        int score = DestinationList.removeUncompleted(selectedDestination, player);
                        player.removeUncompleted(score, selectedDestination);
                        update();
                    } else {
                        status.setText(DestinationList.getPlayerUncompleted(selectedDestination) + " already subtracted this!");
                    }
                } else { // if not completed or uncompleted
                    //check for bonus
                    if (DestinationList.isBonus(selectedDestination)) {
                        int value = DestinationList.parsePoints(selectedDestination);
                        if ((value == 10 && player.hasLongest == false) || (value == 15 && player.hasGlobetrotter == false)) {
                            player.addBonus(value, selectedDestination);
                            update();
                        } else {
                            status.setText(player.getName() + " already has this bonus.");
                        }
                    } else { //if not a bonus
                        int score = DestinationList.addDestination(selectedDestination, player);
                        player.addDestination(score, selectedDestination);
                        update();
                    }
                }
            }
        }

    }//GEN-LAST:event_addButtonActionPerformed

